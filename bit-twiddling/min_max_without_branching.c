#include <stdio.h>
#include <inttypes.h>

/*
 * Computing the min/max of an integer pair without branching.
 *
 *
 * Step 1:    00000000 00000000 00000000 0000101 (5)
 *          ^ 00000000 00000000 00000000 0001000 (8)
 *            ----------------------------------
 *            00000000 00000000 00000000 0001101 (13)
 *
 *
 * Step 2:    5
 *          < 8
 *            -----------------------------------
 *            00000000 00000000 00000000 00000001 (1)
 *
 *
 * Step 3:  - 00000000 00000000 00000000 00000001 (1)
 *            -----------------------------------
 *            11111111 11111111 11111111 11111111 (-1)
 *
 *
 * Step 4:    00000000 00000000 00000000 00001101 (13)
 *          & 11111111 11111111 11111111 11111111 (-1)
 *            -----------------------------------
 *            00000000 00000000 00000000 00001101 (13)
 *
 *
 * Step 5:    00000000 00000000 00000000 00001000 (8)
 * (min)    ^ 00000000 00000000 00000000 00001101 (13)
 *            -----------------------------------
 *            00000000 00000000 00000000 00000101 (5)
 *
 * Step 5:    00000000 00000000 00000000 00000101 (5)
 * (max)    ^ 00000000 00000000 00000000 00001101 (13)
 *            -----------------------------------
 *            00000000 00000000 00000000 00001000 (8)
 */

int main(void)
{
    const int32_t a = 5;
    const int32_t b = 8;

    const int32_t min = b ^ ((a ^ b) & -(a < b));
    const int32_t max = a ^ ((a ^ b) & -(a < b));

    printf("Min: %" PRId32 "\n", min);
    printf("Max: %" PRId32 "\n", max);

    return 0;
}
